package beans;

public class OperationReq {
	

	String action;
	String username;
	


	public OperationReq() {
		super();
	}

	

	public String getUsername() {
		return username;
	}



	public void setUsername(String username) {
		this.username = username;
	}



	public String getAction() {
		return action;
	}


	public void setAction(String action) {
		this.action = action;
	}
	
	
	
	

}
